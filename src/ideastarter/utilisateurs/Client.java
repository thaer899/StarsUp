package ideastarter.utilisateurs;

/**
 *
 * @author attia
 */
public class Client {
    private String Nom;
    private String Prenom;
    private String Pays;
    private String Ville;
    private String Adresse;
    private String Pseudo;
    private String MotDePasse;
    private String TypeCarteBancaire;

    public String getNom() {
        return Nom;
    }

    public void setNom(String Nom) {
        this.Nom = Nom;
    }

    public String getPrenom() {
        return Prenom;
    }

    public void setPrenom(String Prenom) {
        this.Prenom = Prenom;
    }

    public String getPays() {
        return Pays;
    }

    public void setPays(String Pays) {
        this.Pays = Pays;
    }

    public String getVille() {
        return Ville;
    }

    public void setVille(String Ville) {
        this.Ville = Ville;
    }

    public String getAdresse() {
        return Adresse;
    }

    public void setAdresse(String Adresse) {
        this.Adresse = Adresse;
    }

    public String getPseudo() {
        return Pseudo;
    }

    public void setPseudo(String Pseudo) {
        this.Pseudo = Pseudo;
    }

    public String getMotDePasse() {
        return MotDePasse;
    }

    public void setMotDePasse(String MotDePasse) {
        this.MotDePasse = MotDePasse;
    }

    public String getTypeCarteBancaire() {
        return TypeCarteBancaire;
    }

    public void setTypeCarteBancaire(String TypeCarteBancaire) {
        this.TypeCarteBancaire = TypeCarteBancaire;
    }
    
    
    
    
}
